#!/usr/bin/env node
require("dotenv").config({ path: "/home/rommar/estateos/.env" });
const { PrismaClient } = require("@prisma/client");

const prisma = new PrismaClient();
const WINDOW_MS = 14 * 24 * 60 * 60 * 1000;
const AUTO_COMMENT = "Transakcja zakończyła się pomyślnie.";
const AUTO_SOURCE = "SYSTEM_14D_FALLBACK";

function log(level, message, details = {}) {
  console[level === "error" ? "error" : "log"](
    JSON.stringify({
      ts: new Date().toISOString(),
      level,
      job: "reviews-finalization-fallback",
      message,
      ...details,
    })
  );
}

async function createMissingReviewsForDeal(deal) {
  const existing = await prisma.review.findMany({
    where: {
      dealId: deal.id,
      reviewerId: { in: [deal.buyerId, deal.sellerId] },
    },
    select: { reviewerId: true, revieweeId: true },
  });

  const hasBuyer = existing.some((r) => r.reviewerId === deal.buyerId && r.revieweeId === deal.sellerId);
  const hasSeller = existing.some((r) => r.reviewerId === deal.sellerId && r.revieweeId === deal.buyerId);

  const pending = [];
  if (!hasBuyer) pending.push({ reviewerId: deal.buyerId, revieweeId: deal.sellerId });
  if (!hasSeller) pending.push({ reviewerId: deal.sellerId, revieweeId: deal.buyerId });

  let created = 0;
  let skipped = 0;
  for (const item of pending) {
    try {
      await prisma.review.create({
        data: {
          dealId: deal.id,
          reviewerId: item.reviewerId,
          revieweeId: item.revieweeId,
          rating: 5,
          comment: AUTO_COMMENT,
          isAutoGenerated: true,
          source: AUTO_SOURCE,
        },
      });
      created += 1;
    } catch (error) {
      if (error && error.code === "P2002") {
        skipped += 1;
      } else {
        throw error;
      }
    }
  }

  return { created, skipped };
}

async function main() {
  const threshold = new Date(Date.now() - WINDOW_MS);
  const deals = await prisma.$queryRawUnsafe(
    `
      SELECT d.id, d.buyerId, d.sellerId, d.finalizedAt, d.updatedAt
      FROM Deal d
      WHERE d.status IN ('FINALIZED', 'CLOSED', 'COMPLETED', 'SOLD')
        AND COALESCE(d.finalizedAt, d.updatedAt) <= ?
      ORDER BY d.updatedAt ASC
      LIMIT 500
    `,
    threshold
  );

  let created = 0;
  let skipped = 0;
  log("info", "scan_started", { scanned: deals.length, threshold: threshold.toISOString() });

  for (const deal of deals) {
    const outcome = await createMissingReviewsForDeal(deal);
    created += outcome.created;
    skipped += outcome.skipped;
  }

  log("info", "scan_completed", { scanned: deals.length, created, skipped });
}

main()
  .catch((error) => {
    log("error", "scan_failed", { error: error instanceof Error ? error.message : String(error) });
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
